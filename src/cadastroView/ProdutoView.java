                habilitarPreco(true);
                jlValorVenda.setText(jtfValorVenda.getText());
                jtfPeso.setText(prod.getPeso());
                jtfPorcentagem.setText(new DecimalFormat("#,##0.0").format(prod.getPorcento_comissao()));

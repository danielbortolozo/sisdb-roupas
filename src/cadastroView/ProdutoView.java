    private void carregaTable(){
        DefaultTableModel amodel = (DefaultTableModel) jTable1.getModel();
        amodel.setNumRows(0);
        ProdutoDAO proDAO = new ProdutoDAO(); 
        try{
           listaProduto = null;        
           listaProduto = proDAO.listaProduto();        
           for (Produto produto : listaProduto){
               amodel.addRow(new Object[]{produto.getId(),produto.getDescricao(), produto.getMarca().getDescricao(), produto.getUnidade().getSigla(),produto.getEstoque(), produto.getVlVenda(),  produto.getCategoria().getDescricao(), produto.getCodBarras(), produto.getCod_interno(), produto.getPrateleira()});            
           }
        }catch (Exception ex){
             JOptionPane.showMessageDialog(null, "Problemas ao Carregar Tabela de Produto"+ex.getMessage());
             ex.printStackTrace();
        }
    }
    
    private void  carregarGradeProduto(Produto produto) {
        DefaultTableModel amodelGrade = (DefaultTableModel) jTableGrade.getModel();
        amodelGrade.setNumRows(0);      
        try{
                                
            for (ProdutoGrade pg : produto.getListaGrade()) {                
                amodelGrade.addRow(new Object[]{pg.getGrade(), pg.getId()});
            }
            
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problema ao carregar tabela de grade"+e.getMessage());
            e.printStackTrace();
        }
        
    }

    
    
